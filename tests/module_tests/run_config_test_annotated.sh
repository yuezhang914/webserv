#!/usr/bin/env bash

# ==============================================================================
# 初学者阅读说明
# ==============================================================================
# 这是一份“只增加注释、不改变原命令”的学习版脚本。
# 每条可执行语句前都有“原第 N 行”说明；跨行命令会在整块命令前逐行解释，
# 避免把注释插入反斜杠续行中而破坏 Shell 语法。
#
# 常用符号速查：
#   #          注释开始；本行后面的内容不执行。
#   $VAR       展开变量 VAR 的值。
#   ${VAR}     用花括号明确变量名边界。
#   ${VAR:-x}  VAR 未设置或为空时使用默认值 x，但不会修改 VAR。
#   $0         当前脚本路径；$1、$2 是函数或脚本的位置参数。
#   $?         上一条命令的退出状态；0 表示成功，非 0 表示失败。
#   $(命令)    命令替换：执行命令并把它的标准输出放到当前位置。
#   "..."      双引号：允许 $变量 展开，同时避免空格、* 等被再次拆分或展开。
#   '...'      单引号：内容完全按字面处理，不展开变量。
#   \          行末反斜杠表示下一物理行仍属于同一条命令。
#   ;          在同一物理行结束一条命令，例如 `; then`。
#   &&         左侧成功后才执行右侧。
#   ||         左侧失败后才执行右侧。
#   !          对命令或测试结果取反。
#   |          管道：把左侧标准输出交给右侧标准输入。
#   >文件      覆盖写入标准输出。
#   >>文件     追加写入标准输出。
#   2>文件     重定向文件描述符 2，也就是标准错误。
#   2>&1       让标准错误指向标准输出当前去向。
#   >&2        把本命令标准输出送到标准错误。
#   /dev/null  丢弃写进去的内容。
#   [ 条件 ]   `test` 命令；`[` 与 `]` 两边必须有空格。
#   (...)      子 Shell 或 Bash 数组，具体含义取决于上下文。
#   {...}      当前 Shell 中的命令组；花括号两侧需要空格或换行。
#
# 常用退出码：
#   0          成功/条件为真。
#   非 0       失败/条件为假；本脚本常用 1 表示检查失败、2 表示编译失败。
# ==============================================================================
# Bash 专属补充：
#   ARRAY=(a b)       定义数组。
#   "${ARRAY[@]}"     把数组中每个元素分别展开，并保持每个元素的边界。
#   ${PIPESTATUS[0]}  取得最近一个管道中第一个命令的退出码。
# ==============================================================================

# 原第 2 行说明：`set` 修改 Shell 运行选项；`-u` 表示读取未定义变量时立即报错，避免空变量悄悄继续执行。
set -u

# 原第 4 行说明：计算并保存脚本自身所在目录的绝对路径。`$0` 是脚本路径，`dirname` 取父目录，`$(...)` 捕获命令输出；`CDPATH=` 临时清空 cd 搜索路径，`cd --` 中 `--` 结束选项解析，`&&` 仅在 cd 成功后执行 `pwd`。
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
# 原第 5 行说明：计算项目根目录。`$(...)` 是命令替换；`../..` 从测试脚本目录向上两级，`&&` 保证前一个 `cd` 成功后才执行 `pwd`；双引号保护路径空格。
ROOT_DIR="$(cd "$SCRIPT_DIR/../.." && pwd)"
# 原第 6 行说明：保存测试可执行文件路径；文件名前的 `.` 使它在 Unix 中成为隐藏文件。
BINARY="$SCRIPT_DIR/.config_module_test"
# 原第 7 行说明：保存测试结果文本文件路径，后面 `tee` 会同时把输出写入这里。
RESULT_FILE="$SCRIPT_DIR/config_test_results.txt"

# 原第 9 行说明：选择 C++ 编译器。`${CXX:-c++}` 表示：环境变量 `CXX` 已设置且非空时使用它，否则使用默认值 `c++`；`:-` 是带默认值的参数展开。
CXX="${CXX:-c++}"
# 原第 10 行说明：保存编译选项字符串：`-Wall` 开启常见警告，`-Wextra` 开启额外警告，`-Werror` 把警告当错误，`-std=c++98` 强制 C++98；`-pedantic-errors`（若出现）把非标准扩展视为错误；`-I目录` 添加头文件搜索目录。
CXXFLAGS="-Wall -Wextra -Werror -std=c++98"

# 原第 12 行说明：开始定义 Bash 数组。圆括号 `(...)` 包含多个数组元素；后续用 `${数组名[@]}` 可逐个、安全地展开所有元素。
SOURCES=(
# 原第 13 行说明：这是前面源码/头文件列表或编译命令中的一个路径参数；Shell 会把它作为独立文件传给检查命令或编译器。
    "$SCRIPT_DIR/config_module_test.cpp"
# 原第 14 行说明：这是前面源码/头文件列表或编译命令中的一个路径参数；Shell 会把它作为独立文件传给检查命令或编译器。
    "$ROOT_DIR/srcs/Config/Config.cpp"
# 原第 15 行说明：这是前面源码/头文件列表或编译命令中的一个路径参数；Shell 会把它作为独立文件传给检查命令或编译器。
    "$ROOT_DIR/srcs/Config/ConfigParser.cpp"
# 原第 16 行说明：这是前面源码/头文件列表或编译命令中的一个路径参数；Shell 会把它作为独立文件传给检查命令或编译器。
    "$ROOT_DIR/srcs/Config/ConfigRouteUtils.cpp"
# 原第 17 行说明：这是前面源码/头文件列表或编译命令中的一个路径参数；Shell 会把它作为独立文件传给检查命令或编译器。
    "$ROOT_DIR/srcs/Config/LocationConfig.cpp"
# 原第 18 行说明：这是前面源码/头文件列表或编译命令中的一个路径参数；Shell 会把它作为独立文件传给检查命令或编译器。
    "$ROOT_DIR/srcs/Config/ServerConfig.cpp"
# 原第 19 行说明：结束前面 Bash 数组的圆括号列表。
)

# 原第 21 行说明：开始定义 Bash 数组。圆括号 `(...)` 包含多个数组元素；后续用 `${数组名[@]}` 可逐个、安全地展开所有元素。
INCLUDES=(
# 原第 22 行说明：向 Bash 数组加入一个编译参数；`-I目录` 把目录加入 C/C++ 头文件搜索路径，`-I` 与路径之间可以没有空格。
    -I"$ROOT_DIR/includes"
# 原第 23 行说明：继续加入 `-I` 头文件搜索目录；双引号只包住路径部分，展开后仍是一个完整数组元素。
    -I"$ROOT_DIR/srcs"
# 原第 24 行说明：这是前面源码/头文件列表或编译命令中的一个路径参数；Shell 会把它作为独立文件传给检查命令或编译器。
    -I"$ROOT_DIR/srcs/Config"
# 原第 25 行说明：这是前面源码/头文件列表或编译命令中的一个路径参数；Shell 会把它作为独立文件传给检查命令或编译器。
    -I"$ROOT_DIR/srcs/Utils"
# 原第 26 行说明：结束前面 Bash 数组的圆括号列表。
)

# 原第 28 行说明：定义 `cleanup` 函数并在同一行用 `{` 开始函数体；该函数没有显式形参，使用全局变量 `BINARY`。
cleanup() {
# 原第 29 行说明：`rm` 删除文件或目录；`-f` 强制删除且文件不存在时不报错。变量放在双引号中可防止路径中的空格被拆开。
    rm -f "$BINARY"
# 原第 30 行说明：`}` 结束函数体或命令组。
}
# 原第 31 行说明：`trap` 为退出或信号注册清理函数。`EXIT` 是正常/异常退出，`HUP`、`INT`、`TERM` 分别是挂起、中断和终止信号；收到时先执行清理。
trap cleanup EXIT

# 原第 33 行说明：`printf` 按格式输出。`%s` 放入字符串参数，`\n` 表示换行；与 `echo` 相比，格式和转义行为更可预测。
printf '%s\n' "[1/2] Compiling Config strict module tests..."
# 原第 34 行说明：开始条件判断；`!` 取反后面命令的退出状态，因此只有命令失败时条件才为真；`; then` 中分号结束条件命令，随后进入真分支。
if ! "$CXX" $CXXFLAGS "${INCLUDES[@]}" "${SOURCES[@]}" -o "$BINARY"; then
# 原第 35 行说明：`printf` 按格式输出。`%s` 放入字符串参数，`\n` 表示换行；与 `echo` 相比，格式和转义行为更可预测。
    printf '%s\n' "[ERROR] Compilation failed."
# 原第 36 行说明：`exit 2` 立即结束整个脚本，并把状态码 2 返回给调用者；0 通常表示成功，非 0 表示失败。
    exit 2
# 原第 37 行说明：`fi` 是 `if` 的反向拼写，用来结束当前条件分支。
fi

# 原第 39 行说明：`printf` 按格式输出。`%s` 放入字符串参数，`\n` 表示换行；与 `echo` 相比，格式和转义行为更可预测。
printf '%s\n' "[2/2] Running tests..."
# 原第 40 行说明：`+e` 临时关闭 `errexit`，允许下一条命令即使失败也继续执行，以便手工保存退出码。
set +e
# 原第 41 行说明：运行测试程序；`|` 把标准输出传给 `tee`，`tee` 一边显示、一边写结果文件。管道默认返回最后一个命令的状态，因此后面使用 `PIPESTATUS` 取回测试程序状态。
"$BINARY" | tee "$RESULT_FILE"
# 原第 42 行说明：保存管道中第一个命令的退出码。`PIPESTATUS` 是 Bash 数组，`[0]` 取测试程序的状态，而不是 `tee` 的状态；这是 Bash 专属语法。
STATUS=${PIPESTATUS[0]}
# 原第 43 行说明：`-e` 重新开启 `errexit`：后续未处理的非零退出码会终止脚本。
set -e

# 原第 45 行说明：`printf` 按格式输出。`%s` 放入字符串参数，`\n` 表示换行；与 `echo` 相比，格式和转义行为更可预测。
printf '\nResults saved to: %s\n' "$RESULT_FILE"
# 原第 46 行说明：`exit "$STATUS"` 立即结束整个脚本，并把状态码 "$STATUS" 返回给调用者；0 通常表示成功，非 0 表示失败。
exit "$STATUS"
